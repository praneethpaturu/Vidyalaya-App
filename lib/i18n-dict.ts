/** Reverse dictionary so an i18n-aware page can map back if needed. */
export const EN: Record<string, string> = Object.fromEntries(
  Object.entries(HI).map(([en, hi]) => [hi, en]),
);

export type Locale = "en" | "hi";

/** Translate a single string. Identity if locale = en or no entry. */
export function tr(s: string, locale: Locale): string {
  if (locale === "en") return s;
  return HI[s] ?? s;
}
